package Main;

public class App {

    public static void main(String[] args) {

        int sum1 = 0;

        for (int i = 1; i < 7; i++) {
            sum1 += i;
            System.out.println(i + ") Num is " + i + ", sum is " + sum1);
        }
        System.out.println("--------------------\n" + "Sum of number is " + sum1);
    }}



